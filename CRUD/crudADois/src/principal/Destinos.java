package principal;

public class Destinos {
	private int id;
	private String nomeDest;
	
	
	public Destinos(int id, String nomeDest) {
		this.id = id;
		this.nomeDest = nomeDest;
	}
	
	public Destinos() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNomeDest() {
		return nomeDest;
	}

	public void setNomeDest(String nomeDest) {
		this.nomeDest = nomeDest;
	}
	
	
	
	
	
}
